package one;

public class Shorthand {
	public static void main(String args[]) {
		int a =10;
		int b =2, c = 3,d =3;
		a +=5;
		b -=2;
		c *=1;
		d /=3;
		System.out.println("a+ = " +a);
		System.out.println("b- = " +b);
		System.out.println("c* = " +c);
		System.out.println("d/ = " +d);
		
		
	}
	

}
